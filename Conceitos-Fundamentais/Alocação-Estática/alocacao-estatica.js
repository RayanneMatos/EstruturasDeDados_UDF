// Simulação de alocação estática em JavaScript

//Alocação Estática em JavaScript é simulada por variáveis com valores fixos e tipos predefinidos.


const idade = 30;        // Número inteiro, tipo fixo
const nome = "João";     // String, tipo fixo
const altura = 1.75;     // Número de ponto flutuante, tipo fixo

console.log("Nome:", nome);
console.log("Idade:", idade);
console.log("Altura:", altura);

// Aqui, as variáveis estão com seus tipos e tamanhos fixados,
// e não mudam durante a execução do programa.
